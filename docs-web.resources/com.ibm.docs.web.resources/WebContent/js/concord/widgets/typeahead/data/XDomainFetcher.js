dojo.provide("concord.widgets.typeahead.data.XDomainFetcher");
dojo.require("dojo.io.script");

/**
** XDomainFetcher is expected to be mixed in with another dojo.data Store
** it provides a custom _fetchItems function that loads data using dojo.io.script
** once loaded the data Store's _filter method is called with search parameters 
**/

dojo.declare("concord.widgets.typeahead.data.XDomainFetcher", 
	null,
	{
		
	constructor: function(){
		console.debug("XDomainFetcher.constructor()");
	},
	
	_filter: function(){
		console.debug("unimplemented, subclass must override _filter");
	},
	
	_fetchItems: function(	/* Object */ keywordArgs, 
							/* Function */ findCallback, 
							/* Function */ errorCallback){
		//	summary: 
		//		See dojo.data.util.simpleFetch.fetch()
		var self = this;
		
		var filter = dojo.hitch(this, "_filter");

		if(this._loadFinished){
			filter(keywordArgs, this._getItemsArray(keywordArgs.queryOptions), findCallback);
		}else{

			if(this._jsonFileUrl){
				//If fetches come in before the loading has finished, but while
				//a load is in progress, we have to defer the fetching to be 
				//invoked in the callback.
				if(this._loadInProgress){
					this._queuedFetches.push({args: keywordArgs, filter: filter});
				}else{
					this._loadInProgress = true;
					var getArgs = {
							url: self._jsonFileUrl,
							content: {token: self.token, strict: self.strict ? 1 : 0},
							callbackParamName: "jsonp"
						};
					var getHandler = dojo.io.script.get(getArgs);
					getHandler.addCallback(function(data){
						try{
							self._getItemsFromLoadedData(data);
							self._loadFinished = true;
							self._loadInProgress = false;
							
							filter(keywordArgs, self._getItemsArray(keywordArgs.queryOptions), findCallback);
							self._handleQueuedFetches();
						}catch(e){
							self._loadFinished = true;
							self._loadInProgress = false;
							errorCallback(e, keywordArgs);
						}
					});
					getHandler.addErrback(function(error){
						self._loadInProgress = false;
						errorCallback(error, keywordArgs);
					});
				}
			}else if(this._jsonData){
				try{
					this._loadFinished = true;
					this._getItemsFromLoadedData(this._jsonData);
					this._jsonData = null;
					filter(keywordArgs, this._getItemsArray(keywordArgs.queryOptions), findCallback);
				}catch(e){
					errorCallback(e, keywordArgs);
				}
			}else{
				errorCallback(new Error("dojo.data.ItemFileReadStore: No JSON source data was provided as either URL or a nested Javascript object."), keywordArgs);
			}
		}
	}
});

