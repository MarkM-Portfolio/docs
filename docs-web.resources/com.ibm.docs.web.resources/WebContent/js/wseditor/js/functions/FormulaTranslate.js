	// dynamic construct
	de2en : null ,
	ptBR2en : null,
	
	// construct the mapping of LANG to ENGLISH on demand
	_getLANGFormulaMap:function(locale){
		// for unsupported locale, fallback to en2en instead
		var fMap = this.en2en;
		// TODO add more language support here
		if(locale.indexOf("de") != -1) {
		    if (!this.de2en) {
				this.de2en = {};
				for(var key in this.en2de) {
					this.de2en[this.en2de[key]] = key;
				}
			}
			fMap = this.de2en;
		} else if (locale.indexOf("pt-br") != -1) {
		    if (!this.ptBR2en) {
				this.ptBR2en = {};
				for(var key in this.en2ptBR) {
					this.ptBR2en[this.en2ptBR[key]] = key;
				}
			}
			fMap = this.ptBR2en;
		}
		
		return fMap;
	},
	
	_getENFormulaMap: function(locale) {
		// for unsupported locale, fallback to en2en instead
		var fMap = this.en2en;
		// TODO add more language support here
		if (locale.indexOf("de") != -1) {
			fMap = this.en2de;
		} else if (locale.indexOf("pt-br") != -1) {
			fMap = this.en2ptBR;
		}
		
		return fMap;
	},
	
	// translate English to locale language for show
	transFuncNameEn2Locale:function(text,locale){
		if(!locale) {
			var editor = websheet.model.ModelHelper.getEditor();
			locale = editor ? editor.scene.getLocale() : pe.scene.getLocale();
		}
		var fname = new String(text).toUpperCase();
		var fMap = this._getENFormulaMap(locale);
		return fMap[fname];
	},
	
	isSupportedFormula:function(text){
		return (text != null) ? this.en2en[text.toUpperCase()] : null;
	},
	
	// translate locale language to English for parse
	transFuncNameLocale2En:function(text,locale){
		if(!locale) {
			var editor = websheet.model.ModelHelper.getEditor();
			locale = editor ? editor.scene.getLocale() : pe.scene.getLocale();
		}

		var fname = new String(text).toUpperCase();
		var fMap = this._getLANGFormulaMap(locale);
		return fMap[fname];
	},

